// Arrow function==it is a modern and a shorter way of defining functions in javascript
const sayhello=function(){
    console.log("Hello world")
}
sayhello()

const sayHello =()=>{
    console.log("hello world")
}
sayHello()
// create an arrow function to log a message saying good bye
const saygoodbye =()=>{
    console.log("goodbye")
}
saygoodbye()